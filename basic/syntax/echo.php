<?php

	//深入理解echo ,echo是一个函数
	
	//echo 功能：向浏览器输出字符串
	//echo 返回值：void 无返回值

	echo "吴祁刚刚喝了水！";
	
	
	//print 功能：向浏览器输出字符串
	//print 返回值： 整型
	//print将字符串打印出来，而echo 又将print返回值打印出来
	
	echo print "吴祁听的很入神！";
	
	//printf  功能：向浏览器输出字符串
	//printf 返回值：返回字符串的长度
	echo printf ("吴祁今天买了%d套光盘",5);
	
	echo "<br />";  //HTML的还行功能
	
	//sprintf 功能，将字符串保留在内存中
	//sprintf 返回值：返回字符串
	
	echo sprintf ("吴祁今天买了%d套光盘",5);

?>











